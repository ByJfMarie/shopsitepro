    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // Store a new offer

        $newOffer = new Offer;

        $newOffer->title = $request->get('title');
        $newOffer->description = $request->get('description');
        $newOffer->icone = $request->get('icone');
        $newOffer->price = $request->get('price');
        $newOffer->description_inline = json_encode($request->get('description_inline'));

        if ($newOffer->save()) {
            return response()->json(['success' => true, 'message' => 'Offer created']);
        } else {
            return response()->json(['success' => false, 'message' => 'Error creating offer']);
        }


                
    }

